                    </div>
                    `;
                    }).join('');

                    container.innerHTML += `
                    <div class="carousel-item ${i === 0 ? 'active' : ''}">
                    <div class="row justify-content-center px-4">
                        ${slideItems}
                    </div>
                    </div>
